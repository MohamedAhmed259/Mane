const setLanguage = (language) => {
    const elements = document.querySelectorAll("[data-attr]");
    elements.forEach((element) => {
        const attribute = element.getAttribute("data-attr");
        if (translations[language][attribute]) {
            // console.log(`Setting ${attribute} to ${translations[language][attribute]}`); // Debugging line
            element.innerHTML = translations[language][attribute];
        }
    });
    document.documentElement.setAttribute("dir", language === "ar" ? "rtl" : "ltr");
};

const savedLanguage = localStorage.getItem("language") || "en";
setLanguage(savedLanguage);

const languageSelectors = document.querySelectorAll(".btn-en-glish, .btn-ar-abic, .btn-en, .btn-ar");
// console.log(languageSelectors);



let btn_en_glish = document.querySelector(".btn-en-glish"); // en
let btn_ar_abic = document.querySelector(".btn-ar-abic");  // ar

// دالة لتحديث العرض بناءً على اللغة وحجم الشاشة
function updateDisplay() {
    // الحصول على اللغة المخزنة في كل مرة يتم فيها استدعاء هذه الدالة
    const storedLanguage = localStorage.getItem("language");
    const isSmallScreen = window.matchMedia("(max-width: 992px)").matches; // حجم الشاشة الصغيرة

    if (storedLanguage === "ar") {
        btn_en_glish.innerHTML = isSmallScreen ? "En" : "English";
        btn_ar_abic.style.display = "none";
        btn_en_glish.style.display = "block";
    } else if (storedLanguage === "en") {
        btn_ar_abic.innerHTML = isSmallScreen ? "عر" : "العربية";
        btn_en_glish.style.display = "none";
        btn_ar_abic.style.display = "block";
    }

};
updateDisplay();

languageSelectors.forEach(selector => {
    selector.addEventListener("click", (event) => {
        const language = event.target.dataset.attr === "language-ar" ? "ar" : "en";
        setLanguage(language);
        localStorage.setItem("language", language);

        updateDisplay(); // تحديث العرض بعد تغيير اللغة
    });
});

// تحديث العرض عند تغيير حجم الشاشة
window.addEventListener("resize", updateDisplay);
